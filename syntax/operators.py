# two multiplication symbols makes a power relationship
squared = 7 ** 2
cubed = 2 ** 3
print(squared)
print(cubed)

# concatenation of strings using the addition operator
helloworld = "hello" + " " + "world"
print(helloworld)

# multiply strings
lotsofhellos = "hello" * 10
print(lotsofhellos)

# Lists can be joined with the addition operator
even_numbers = [2,4,6,8]
odd_numbers = [1,3,5,7]
all_numbers = odd_numbers + even_numbers
print(all_numbers)

#  forming new lists with a repeating sequence using the multiplication
print([1,2,3] * 3)